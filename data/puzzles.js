/**
 * Returns a random puzzle based on the selected difficulty.
 * @param {string} difficulty - The selected difficulty level.
 * @returns {string} A string of 81 characters.
 */
function getRandomPuzzle(difficulty = "medium") {
  const selectedPuzzles = puzzles[difficulty] || puzzles.medium;
  return selectedPuzzles[Math.floor(Math.random() * selectedPuzzles.length)];
}

